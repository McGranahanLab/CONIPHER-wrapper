#!/bin/bash

################################################################################## Input parameters
###################################################################################################

case_id="CRUKTOY001"
scriptDir=`pwd`"/src/"
inputTSV=`pwd`"/data/input_tsv.tsv"
outDir=`pwd`"/data/results/"


###################################################################### Running treebuilding wrapper
###################################################################################################

